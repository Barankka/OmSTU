public class Pizza {
    String name;
    String sauce;
    String stuff;

    public String getName()
    {
        return name;
    }
    public String getSauce()
    {
        return sauce;
    }
    public String getStuff()
    {
        return stuff;
    }

    public void setName(String n)
    {
        name = n;
    }
    public void setSauce(String s)
    {
        sauce = s;
    }
    public void setStuff(String st)
    {
        stuff = st;
    }

}
